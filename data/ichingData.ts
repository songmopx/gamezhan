export const getLineName = (type: number, position: number): string => {
  const lineType = type === 1 ? '九' : '六';
  if (position === 1) return `初${lineType}`;
  if (position === 6) return `上${lineType}`;
  const posNames = ['', '初', '二', '三', '四', '五', '上'];
  return `${lineType}${posNames[position]}`;
};
